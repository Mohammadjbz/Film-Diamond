export default function convertLanguage(language) {
  switch (language) {
    case "en":
      return "English";
    case "hi":
      return "Hindi";
    case "es":
      return "Spanish";
    case "fr":
      return "french";
    case "fa":
      return "Persian";
    case "de":
      return "German";
    case "it":
      return "Italian";
    case "ru":
      return "Russian";
    case "ja":
      return "Japanese";
    case "tr":
      return "Turkish";
  }
}
